void main() {

  double num1 = 20;
  double num2 = 8;

  double sum = num1 + num2;
  print(sum);

  double minus = num1 - num2;
  print(minus);

  double times = num1 * num2;
  print(times);

  double division = num1 / num2;
  print(division);

  // increment
  num1 = num1 + 50;
  num1 += 50;
  num1++;

  // decrement
  num1--;
  print(num1);

  // rest of division
  num1++;
  var rest = num1 % 2;
  print(rest);
  
}
