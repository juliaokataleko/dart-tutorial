void main() {
  // Comparadores
  // > Maior
  // < Menor
  // >= Maior ou igual
  // <= Menor ou igual
  // == Igual
  // != Diferente

  var valor = true;

  if (valor == true) {
    print("Valor $valor");
  } else  if (valor == false) {
    print("Valor $valor");
  } else {
    print("O valor $valor nao e verdadeiro e nem falso");
  }

}
